
x = int(input("Enter the value of X \n"))

if x == 1:
    print("One") 
elif x == 2:
    print("Two") 
elif x == 3:
    print("Three") #// elif will not check other statements if the condition satisfies earlier 
elif x == 4:
    print("Four")
elif x == 5:
    print("Five")

else:
    print("Enter value of X under 5")


